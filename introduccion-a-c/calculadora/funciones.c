#include <stdio.h>
#include "funciones.h"

float sumar(float numa, float numb)
{
    return numa + numb;
};
float restar(float numa, float numb)
{
    return numa - numb;
};
float multiplicar(float numa, float numb)
{
    return numa * numb;
};
float dividir(float numa, float numb)
{
    return numa / numb;
};